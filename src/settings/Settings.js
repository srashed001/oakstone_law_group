function calculateCurrentForm(results, max = 1, min = 131) {
  let currentForm = 0;

  for (let result of results) {
    if (result.outcome === "w") {
      currentForm +=
      (0.2 * (result.margin / 7) * (min - result.opponentRank)) /
        (min - max);
    } else {
      currentForm -=
        (0.2 * (result.margin / 7) * (max - result.opponentRank)) /
        (max - min);
    }

    if (result.spread === "w") {
      currentForm += (0.4 * (result.sMargin / 7) * (min - result.opponentRank)) /
      (min - max);
    } else {
        currentForm -= (0.4 * (result.sMargin / 7) * (max - result.opponentRank)) /
        (max - min);
    }
  }
  // console.log(`currentForm: ${currentForm}`)

 

  return currentForm;
}

function calculateWinningProbability({
  teamStats,
  opponentStats,
  currentForm,
  refereeBias,
}) {
  // Calculate the team's strength based on their stats and the individual stats of their members
  let teamStrength = teamStats.OFEI * 0.15;
  teamStrength += teamStats.ODE * 0.1;
  teamStrength += teamStats.OTD * 0.2;
  teamStrength += teamStats.OVD * 0.1;
  teamStrength += teamStats.OFD * 0.15;
  teamStrength += 1 - teamStats.OBD * 0.2;
  teamStrength += 1 - teamStats.OTO * 0.2;
  teamStrength += teamStats.DFEI * 0.15;
  teamStrength += teamStats.DDE * 0.1;
  teamStrength += 1 - teamStats.DTD * 0.2;
  teamStrength += 1 - teamStats.DVD * 0.1;
  teamStrength += 1 - teamStats.DFD * 0.15;
  teamStrength += teamStats.DBD * 0.2;
  teamStrength += teamStats.DTO * 0.2;
  if (teamStats.isHomeTeam) {
    teamStrength += 0.1;
  }

  // Calculate the opponent's strength based on their stats and the individual stats of their members
  let opponentStrength = opponentStats.OFEI * 0.15;
  opponentStrength += opponentStats.ODE * 0.1;
  opponentStrength += opponentStats.OTD * 0.2;
  opponentStrength += opponentStats.OVD * 0.1;
  opponentStrength += opponentStats.OFD * 0.15;
  opponentStrength += 1 - opponentStats.OBD * 0.2;
  opponentStrength += 1 - opponentStats.OTO * 0.2;
  opponentStrength += opponentStats.DFEI * 0.15;
  opponentStrength += opponentStats.DDE * 0.1;
  opponentStrength += 1 - opponentStats.DTD * 0.2;
  opponentStrength += 1 - opponentStats.DVD * 0.1;
  opponentStrength += 1 - opponentStats.DFD * 0.15;
  opponentStrength += opponentStats.DBD * 0.2;
  opponentStrength += opponentStats.DTO * 0.2;

  if (opponentStats.isHomeTeam) {
    opponentStrength += 0.1;
  }

  // console.log(`team: ${teamStrength}`)
  // console.log(`opponent: ${opponentStrength}`)

  teamStrength += calculateCurrentForm(currentForm.team);
  opponentStrength += calculateCurrentForm(currentForm.opponent);

  teamStrength -= teamStats.playersMissing ? teamStats.playersMissing * .1 : 0
  opponentStrength -= opponentStats.playersMissing ? opponentStats.playersMissing * .1 : 0

  let probability =
    teamStrength / (teamStrength + opponentStrength + refereeBias);

  return probability * 100;
}


